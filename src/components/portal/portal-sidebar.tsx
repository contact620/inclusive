"use client";

import Image from "next/image";
import Link from "next/link";
import { usePathname } from "next/navigation";
import { cn } from "@/lib/utils";

const navigation = [
  { name: "Mon espace", href: "/portal", icon: HomeIcon },
  { name: "Mes projets", href: "/portal/projects", icon: ProjectIcon },
  { name: "Mes documents", href: "/portal/documents", icon: DocumentIcon },
  { name: "Notifications", href: "/portal/notifications", icon: BellIcon },
  { name: "Mon profil", href: "/portal/profile", icon: UserIcon },
];

export function PortalSidebar({ className, unreadCount = 0 }: { className?: string; unreadCount?: number }) {
  const pathname = usePathname();

  return (
    <aside
      className={cn(
        "flex h-full w-64 flex-col border-r border-gray-200 bg-white",
        className
      )}
    >
      <div className="flex h-16 items-center border-b border-gray-200 px-4">
        <Link href="/portal" className="flex w-full items-center">
          <Image src="/logo.png" alt="Inclusive" width={0} height={0} sizes="100%" className="h-12 w-auto" />
        </Link>
      </div>
      <nav className="flex-1 space-y-1 px-3 py-4">
        {navigation.map((item) => {
          const isActive =
            item.href === "/portal"
              ? pathname === "/portal"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                isActive
                  ? "bg-blue-50 text-blue-700"
                  : "text-gray-700 hover:bg-gray-100"
              )}
            >
              <item.icon className="h-5 w-5" />
              <span className="flex-1">{item.name}</span>
              {item.name === "Notifications" && unreadCount > 0 && (
                <span className="flex h-5 min-w-5 items-center justify-center rounded-full bg-red-500 px-1.5 text-xs font-bold text-white">
                  {unreadCount}
                </span>
              )}
            </Link>
          );
        })}
      </nav>
      <div className="border-t border-gray-200 p-4">
        <p className="text-xs text-gray-400">Espace Client Inclusive</p>
      </div>
    </aside>
  );
}

